# ============================================================
# Day 1：最小 PyTorch 分类训练脚本
# 任务：用一个小神经网络学习二分类
# ============================================================


# ------------------------------------------------------------
# 第一部分：导入库
# ------------------------------------------------------------

import torch                     # PyTorch 主库，提供张量、随机数、自动求导等核心功能
import torch.nn as nn            # 神经网络模块，提供 Linear、ReLU、Loss 等常用组件
import torch.optim as optim      # 优化器模块，提供 Adam、SGD 等参数更新算法


# ------------------------------------------------------------
# 第二部分：固定随机种子
# ------------------------------------------------------------

torch.manual_seed(42)
# 固定随机种子，让每次运行时随机生成的数据和初始参数保持一致
# 这样方便复现结果、对比实验
# 42 只是一个习惯用的数字，换成其他整数也可以


# ------------------------------------------------------------
# 第三部分：构造数据
# ------------------------------------------------------------

X = torch.randn(100, 2)
# 生成输入数据 X
# torch.randn(100, 2) 表示：
#   - 生成一个 shape 为 (100, 2) 的张量
#   - 100 行 = 100 个样本
#   - 2 列  = 每个样本有 2 个特征
#   - 数值来自标准正态分布（均值 0，标准差 1）
#
# 你可以把 X 想象成一张表：
#   每一行是一个样本
#   每一列是一个特征维度


y = (X[:, 0] + X[:, 1] > 0).long()
# 根据 X 人工构造标签 y，拆开理解：
#
#   X[:, 0]         → 取所有样本的第 0 列（第 1 个特征），shape: (100,)
#   X[:, 1]         → 取所有样本的第 1 列（第 2 个特征），shape: (100,)
#   X[:,0] + X[:,1] → 两列相加，shape: (100,)
#   > 0             → 判断每个值是否大于 0，得到布尔张量，True / False
#   .long()         → 把布尔值转成整数：True → 1，False → 0
#
# 最终 y 的 shape 是 (100,)，每个值是 0 或 1
# 这就是我们人为规定的分类规则：
#   如果两个特征之和 > 0，标签为 1
#   否则标签为 0
#
# 为什么要 .long()？
#   CrossEntropyLoss 要求标签是整数类别编号（long 类型）
#   如果不转，后面计算 loss 时会报类型错误


# ------------------------------------------------------------
# 第四部分：定义模型
# ------------------------------------------------------------

model = nn.Sequential(
    nn.Linear(2, 8),   # 第一层：全连接层，输入 2 维，输出 8 维
    nn.ReLU(),         # 激活函数：引入非线性
    nn.Linear(8, 2)    # 第二层：全连接层，输入 8 维，输出 2 维
)
# nn.Sequential 表示按顺序把多个层串联起来
# 数据从上往下依次流过每一层
#
# 整体结构：
#   输入 (100, 2)
#     ↓ nn.Linear(2, 8)   → (100, 8)
#     ↓ nn.ReLU()         → (100, 8)  形状不变，只是负值变 0
#     ↓ nn.Linear(8, 2)   → (100, 2)
#   输出 (100, 2)
#
# 关于 nn.Linear(输入维度, 输出维度)：
#   这是一个全连接层，内部有可学习的权重矩阵 W 和偏置 b
#   计算方式：output = input @ W.T + b
#   这些参数会在训练过程中被不断更新
#
# 关于 nn.ReLU()：
#   ReLU(x) = max(0, x)
#   正数保持不变，负数变成 0
#   作用：给模型引入非线性能力
#   如果没有激活函数，多层线性层叠加本质上还是线性变换，表达能力非常有限
#
# 为什么最后输出是 2？
#   因为这是二分类任务，有类别 0 和类别 1
#   模型对每个样本输出 2 个分数（logits），分别对应两个类别
#   分数越高，模型越认为该样本属于这个类别
#   注意：输出 2 对应的是"类别数"，不是"样本数"


# ------------------------------------------------------------
# 第五部分：定义损失函数
# ------------------------------------------------------------

criterion = nn.CrossEntropyLoss()
# CrossEntropyLoss 是分类任务中最常用的损失函数
#
# 它接收：
#   - logits：模型输出，shape (N, C)，N 是样本数，C 是类别数
#   - y：真实标签，shape (N,)，每个值是整数类别编号
#
# 它做的事情：
#   1. 先对 logits 做 softmax，把分数转成概率分布
#   2. 再计算预测概率和真实标签之间的交叉熵
#   3. 返回一个标量 loss 值
#
# 直觉理解：
#   loss 大 → 模型预测和真实标签差距大
#   loss 小 → 模型预测更接近真实标签
#   训练的目标就是让 loss 尽量小


# ------------------------------------------------------------
# 第六部分：定义优化器
# ------------------------------------------------------------

optimizer = optim.Adam(model.parameters(), lr=0.01)
# 优化器负责根据梯度更新模型参数
#
# optim.Adam：使用 Adam 优化算法
#   Adam 是目前最常用的优化器之一
#   它会自适应地调整每个参数的学习率
#   比基础的 SGD 通常更稳定、收敛更快
#
# model.parameters()：
#   把模型中所有可学习的参数（权重 W 和偏置 b）交给优化器管理
#   优化器需要知道"要更新哪些参数"
#
# lr=0.01：
#   学习率，控制每次参数更新的步长
#   太大：可能跳过最优解，loss 震荡
#   太小：收敛很慢
#   0.01 是一个常见的初始尝试值


# ------------------------------------------------------------
# 第七部分：训练循环
# ------------------------------------------------------------

for epoch in range(20):
    # epoch 表示对整个数据集完整训练一轮
    # range(20) 表示训练 20 轮
    # 每一轮都会：
    #   1. 前向传播得到预测
    #   2. 计算 loss
    #   3. 反向传播算梯度
    #   4. 更新参数


    # ---- 前向传播 ----
    logits = model(X)
    # 把输入数据 X 送进模型，得到输出 logits
    # logits 的 shape：(100, 2)
    #   每个样本对应 2 个类别分数
    # 这一步叫"前向传播"（forward pass）
    # 数据从输入层流向输出层，得到预测结果


    # ---- 计算 loss ----
    loss = criterion(logits, y)
    # 用损失函数计算预测结果和真实标签之间的差距
    # logits：模型输出，shape (100, 2)
    # y：真实标签，shape (100,)
    # loss：一个标量，表示当前模型有多差
    #
    # loss 越大 → 模型预测越不准
    # loss 越小 → 模型预测越接近真实标签


    # ---- 清空旧梯度 ----
    optimizer.zero_grad()
    # PyTorch 默认会把每次 backward 计算出的梯度累加到参数的 .grad 属性上
    # 如果不清空，本轮的梯度会和上一轮的梯度叠加，导致参数更新出错
    # 所以每次反向传播前，先把旧梯度清零
    #
    # 注意：zero_grad 要放在 backward 之前


    # ---- 反向传播 ----
    loss.backward()
    # 根据当前 loss，自动计算模型中每个参数的梯度
    # 使用链式法则（chain rule）从输出层往输入层反向传播
    #
    # 重要区分：
    #   backward() → 只是"算出梯度"，还没有改变参数
    #   step()     → 才是真正"用梯度更新参数"
    #
    # 执行完这行后，每个参数的 .grad 属性里存放了对应的梯度值


    # ---- 参数更新 ----
    optimizer.step()
    # 优化器根据每个参数的 .grad，按照 Adam 算法更新参数值
    # 这一步才是真正把模型往"更好的方向"推了一步
    #
    # 一次完整的更新流程：
    #   zero_grad() → backward() → step()
    # 这三步缺一不可，顺序不能乱


    # ---- 每 5 轮打印一次结果 ----
    if (epoch + 1) % 5 == 0:
        # (epoch + 1) % 5 == 0 表示：当前是第 5、10、15、20 轮时打印
        # epoch 从 0 开始，所以要 +1 才是人类习惯的轮次编号

        pred = logits.argmax(dim=1)
        # logits.argmax(dim=1)：
        #   对每个样本，在 2 个类别分数中取分数最大的那个类别的索引
        #   dim=1 表示在列方向（类别维度）上取 argmax
        #   结果 shape：(100,)，每个值是 0 或 1
        #
        # 例如某个样本的 logits 是 [-0.3, 1.2]
        #   argmax → 1（第 1 个类别分数更高）
        #   预测类别 = 1

        acc = (pred == y).float().mean()
        # pred == y：逐元素比较预测和真实标签，相同为 True，不同为 False
        # .float()：把 True/False 转成 1.0/0.0
        # .mean()：求平均，即正确预测的比例
        # 结果就是准确率 accuracy

        print(f"Epoch {epoch+1:02d} | Loss = {loss.item():.4f} | Acc = {acc.item():.4f}")
        # loss.item()：把 loss 张量里的数值取出来，变成普通 Python float，方便打印
        # acc.item()：同上
        # :.4f：保留 4 位小数
        # :02d：整数至少显示 2 位，不足补 0，例如 5 → 05
