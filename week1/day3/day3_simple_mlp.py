# day3_simple_mlp.py

import torch
import torch.nn as nn

# ============================================================
# Part 1: 构造一个 batch 输入
# ============================================================

# 任务 1-1：
# 创建一个输入张量 x，shape 为 (5, 10)
# 表示 5 个样本，每个样本 10 个特征
x = torch.randn(5, 10)

print("x shape:", x.shape)

# ============================================================
# Part 2: 定义一个最小 MLP
# ============================================================

# 任务 2-1：
# 用 nn.Sequential 定义一个最小 MLP：
# Linear(10, 16) -> ReLU -> Linear(16, 2)
model = nn.Sequential(
  nn.Linear(10, 16),
  nn.ReLU(),
  nn.Linear(16, 2)
)

print(model)

# ============================================================
# Part 3: 前向传播
# ============================================================

# 任务 3-1：
# 把 x 输入模型，得到输出 out
out = model(x)

print("out shape:", out.shape)
print("out:", out)

# ============================================================
# Part 4: 查看每一层对 shape 的影响
# ============================================================

# 任务 4-1：
# 手动拆开每一层，观察 shape 变化
layer1 = model[0]
act = model[1]
layer2 = model[2]

h1 = layer1(x)
h2 = act(h1)
h3 = layer2(h2)

print("after layer1 shape:", h1.shape)
print("after ReLU shape:", h2.shape)
print("after layer2 shape:", h3.shape)

# ============================================================
# Part 5: 参数 shape 检查
# ============================================================

# 任务 5-1：
# 打印第一层和第二层参数的 shape
print("layer1 weight shape:", layer1.weight.shape)
print("layer1 bias shape:", layer1.bias.shape)
print("layer2 weight shape:", layer2.weight.shape)
print("layer2 bias shape:", layer2.bias.shape)

# ============================================================
# Part 6: 回答问题（写在注释里）
# ============================================================

# 问题 1：
# 为什么 Linear(10,16) 会把 shape 从 (5,10) 变成 (5,16)？

# 你的回答：因为我们规定的输出是16维的，每个输入样本经过这个线性层后会被映射到一个16维的空间，所以输出的 shape 就变成了 (5,16)，其中 5 是 batch size，保持不变。

# Best Answer:
# 如果网络中只有线性层，那么多层线性变换组合起来本质上仍然等价于一个线性变换，因此模型只能表示线性关系，表达能力有限。
# 激活函数（如 ReLU）在线性层之间加入了非线性，使网络能够学习更复杂的函数关系，而不只是简单的线性映射。



# 问题 2：
# 如果把中间的 ReLU 去掉，这个网络还算不算“多层”？
# 它和有 ReLU 的网络差别是什么？

# 你的回答：不算，ReLU是一个非线性的激活函数，如果说去除了ReLU，那么这个网络的本质就是一个线性模型，本质上等价于一个线性变换，没有非线性的能力，也即没有学习复杂函数的能力。
# 而有了ReLU之后，网络就具有了非线性的表达能力，可以学习更复杂的函数关系，因此算是一个真正的多层网络。

# Best Answer:
# 在 `Linear(10,16) -> ReLU -> Linear(16,2)` 这个网络中：
# - 输入层对应输入数据的 10 维特征
# - 隐藏层对应第一层线性变换后得到的 16 维中间表示
# - 输出层对应最后一层产生的 2 维输出，用来表示 2 个类别的分数
